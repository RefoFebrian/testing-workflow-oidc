    def get_stock_location(self):
        query = f"""
            SELECT sl.id as location_id
                , SUM(sq.quantity) as qty
            FROM stock_quant sq
            INNER JOIN product_product prod ON sq.product_id = prod.id
            LEFT JOIN product_template pt ON prod.product_tmpl_id = pt.id
            LEFT JOIN stock_location sl on sq.location_id = sl.id
            WHERE sq.quantity > 0
            AND sl.active is true
            AND prod.division = '{self.division}'
            AND sl.company_id = {self.company_id.id}
            AND (sl.usage = 'internal' OR sl.usage = 'transit' OR sl.usage = 'nrfs')
            GROUP BY sl.id
        """
        self.env.cr.execute(query)
        return self.env.cr.dictfetchall()
    
    def get_accessories_stock_location(self):
        query = f"""
            SELECT sl.id as location_id
                , SUM(sq.quantity) as qty
            FROM stock_quant sq
            INNER JOIN product_product prod ON sq.product_id = prod.id
            LEFT JOIN product_template pt ON prod.product_tmpl_id = pt.id
            LEFT JOIN stock_location sl on sq.location_id = sl.id
            WHERE sq.quantity > 0
            AND sl.active is true
            AND prod.division = 'Extras'
            AND sl.company_id = {self.company_id.id}
            AND (sl.usage = 'internal' OR sl.usage = 'transit' OR sl.usage = 'nrfs')
            GROUP BY sl.id
        """
        self.env.cr.execute(query)
        return self.env.cr.dictfetchall()
    
