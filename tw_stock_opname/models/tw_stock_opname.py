    @api.depends('detail_opname_ids.state')
    def _compute_summary_data(self):
        total_data = len(self.detail_opname_ids)
        counted = 0
        not_counted = 0
        anomali = 0
        percentage_counted = 0
        percentage_not_counted = 0
        percentage_anomali = 0
        for record in self.detail_opname_ids:
            if record:
                if record.state == 'open' :
                    not_counted +=1
                if record.state == 'anomali':
                    anomali +=1
                if record.state in ('done', 'selisih'):
                    counted +=1
        
        self.total_data = total_data
        self.counted = counted
        self.not_counted = not_counted
        self.anomali = anomali
        
        if total_data > 0:
            percentage_counted = round((counted / total_data) * 100, 2)
            percentage_not_counted = round((not_counted/total_data) * 100, 2)
            percentage_anomali = round((anomali / total_data) * 100, 2)

        self.counted_percentage = percentage_counted
        self.not_counted_percentage = percentage_not_counted
        self.anomali_percentage = percentage_anomali
    
    @api.depends('company_id')
    def _compute_name(self):
        for record in self:
            record.name = False
            if record.id:
                code = record.company_id.code or 'E'
                prefix = 'SO'
                record.name = record.env['ir.sequence'].suspend_security().get_sequence_code(prefix,code)

