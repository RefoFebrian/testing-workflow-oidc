    def action_done(self):
        self._check_line()
        if not getattr(self, 'approval_ids', None):
            if self.division == 'Unit' and self.open_opname_ids:
                return self.action_open_wizard(self.open_opname_ids)
            elif self.division == 'Extras' and self.open_accessories_opname_ids:
                return self.action_open_wizard(self.open_accessories_opname_ids)
        
        for line in self.detail_opname_ids:
            if line.state == 'open':
                if line.qty_system == 0:
                    line.write({
                            'state':'done'
                            })
                else:
                    raise Warning("Masih Terdapat data unit yang belum dihitung pada lokasi %s\n" %(line.location_id.name))

        for line in self.detail_accessories_ids:
            if line.state == 'open':
                if line.qty_system == 0:
                    line.write({
                        'state': 'done'
                    })
                else:
                    raise Warning("Masih Terdapat data accessories yang belum dihitung pada lokasi %s\n" % (line.location_id.name))

        self.write({
            'state': 'done',
            'done_uid': self._uid,
            'done_date': self._get_default_datetime(),
        })

