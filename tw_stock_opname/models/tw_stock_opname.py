    def _generate_watermark(self, foto_all_stock_binary, text):

        # 1. Decode base64 ke bytes
        image_bytes = base64.b64decode(foto_all_stock_binary)

        # 2. Buka gambar dari bytes
        image = Image.open(io.BytesIO(image_bytes)).convert("RGBA")

        # 2. Buat layer transparan untuk watermark
        txt_layer = Image.new('RGBA', image.size, (255, 255, 255, 0))
        draw = ImageDraw.Draw(txt_layer)

        # 3. Pilih font
        font_path = file_path_util('tw_stock_opname/static/src/font/Inter.ttf')

        # 4. Tentukan teks dan ukurannya
        image_width, image_height = image.size
        font_size = max(10, int(image_height * 0.03))
        font = ImageFont.truetype(font_path, font_size)

        # 5. Pisah teks jadi kata dan bungkus ke baris
        words = text.split()
        lines = []
        current_line = ""

        for word in words:
            test_line = current_line + " " + word if current_line else word
            test_width = draw.textlength(test_line, font=font)
            if test_width <= image_width - 2 * 10:  # padding kiri-kanan
                current_line = test_line
            else:
                lines.append(current_line)
                current_line = word
        if current_line:
            lines.append(current_line)
        
        # 6. Hitung total tinggi teks (termasuk spasi antar baris)
        line_spacing = 5
        total_text_height = sum([
            font.getbbox(line)[3] - font.getbbox(line)[1] + line_spacing
            for line in lines
        ]) - line_spacing

        # 5. Hitung ukuran teks pakai textbbox
        padding = 10
        text_position = (padding, padding)
        background_height = total_text_height
        background_width = image_width - 2 * padding

        # 6. Gambar background transparan hitam di belakang teks
        rect_position = (
            text_position[0] - 5,                          # kiri
            text_position[1] - 5,                          # atas
            text_position[0] + background_width + 5,       # kanan
            text_position[1] + background_height + 5       # bawah
        )
        draw.rectangle(rect_position, fill=(0, 0, 0, 128))  # hitam transparan

        # 7. Gambar teks warna putih
        y = text_position[1]
        for line in lines:
            draw.text((text_position[0], y), line, font=font, fill=(255, 255, 255, 255))  # putih solid
            y += font.getbbox(line)[3] - font.getbbox(line)[1] + line_spacing

        # 8. Gabungkan gambar asli dan watermark
        watermarked = Image.alpha_composite(image, txt_layer)

        # 9. Simpan dan encode ke base64
        buffer = io.BytesIO()
        watermarked.convert("RGB").save(buffer, format="JPEG")
        buffer.seek(0)
        result_base64 = base64.b64encode(buffer.read()).decode('utf-8')

        # Output: base64_string berisi gambar dengan watermark
        return result_base64

