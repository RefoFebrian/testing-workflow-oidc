    @api.constrains('periode_awal','periode_akhir')
    def _check_periode(self):
        for record in self:
            if record.periode_awal and record.periode_akhir:
                if record.periode_akhir < record.periode_awal:
                    raise Warning('Perhatian!\nPeriode akhir tidak boleh kurang dari periode awal.')

    @api.model_create_multi
    def create(self, vals_list):
        for vals in vals_list:
            outstanding_so = self.search([
                ('company_id','=',vals['company_id']),
                ('state','!=','done'),
                ('division','=',vals['division'])
            ])
            if outstanding_so:
                raise Warning('Perhatian!\nAda stock opname yang belum selesai.')
        return super(StockOpname, self).create(vals_list)
        
